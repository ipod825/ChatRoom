    public void addFailed(String reason){
        setVisible(true);
        if (reason.equals("notexist")){
            JOptionPane.showMessageDialog(this, "查無此帳號, 請重新輸入", "", JOptionPane.ERROR_MESSAGE);
            addFriend.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "此帳號已在好友名單, 請重新輸入", "", JOptionPane.ERROR_MESSAGE);
            addFriend.setText("");
        }
    }



