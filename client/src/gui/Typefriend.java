    private void AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddMouseClicked
        String addname = new String(addFriend.getText());
        String groupname = new String(addgroup.getText());
        boolean check1 = false;
        boolean check2 = false;       
        if( addname.equals("") ) {
            JOptionPane.showMessageDialog(this, "請輸入帳號", "", JOptionPane.ERROR_MESSAGE);
        } else if( addname.contains(" ") ) {
            JOptionPane.showMessageDialog(this, "帳號請不要有空白", "", JOptionPane.ERROR_MESSAGE);
            addFriend.setText("");
        } else if(addname.equals(NewMain.NWKSER.friendlist.who)){
            JOptionPane.showMessageDialog(this, "你不能新增自己為好友", "", JOptionPane.ERROR_MESSAGE);
            addFriend.setText("");
        }
        else {
            check1 = true;
        }
        
        if( groupname.equals("") ) {
            JOptionPane.showMessageDialog(this, "請輸入群組", "", JOptionPane.ERROR_MESSAGE);
        } else if( groupname.contains(" ") ) {
            JOptionPane.showMessageDialog(this, "群組名稱請不要有空白", "", JOptionPane.ERROR_MESSAGE);
            addgroup.setText("");
        } else {
            check2 = true;
        }

        if(check1==true&&check2==true){
            JOptionPane.showMessageDialog(this, "已送出邀請,請等待對方回覆確認為好友", "", JOptionPane.PLAIN_MESSAGE);
            NewMain.NWKSER.sendmsg("/addfriend "+addname+" "+groupname);
            setVisible(false);
        }
    }//GEN-LAST:event_AddMouseClicked

